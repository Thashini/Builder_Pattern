class Car {
    String make;
    String model;
    int year;
    String color;
    int engineSize;
    int doors;

    public String getMake() {
        return make;
    }

    public String getModel() {
        return model;
    }

    public int getYear() {
        return year;
    }

    public String getColor() {
        return color;
    }

    public int getEngineSize() {
        return engineSize;
    }

    public int getDoors() {
        return doors;
    }
}
